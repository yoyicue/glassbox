def _scene(page_id, scene_type, items):
    return Scene(
        frame_id=0,
        timestamp=0.0,
        viewport_size=(640, 990),
        page_id=page_id,
        scene_type=scene_type,
        elements=[
            UIElement(text=t, type="text", box=Box(x=x, y=y, w=w, h=h), confidence=1.0)
            for (t, x, y, w, h) in items
        ],
    )


@pytest.mark.smoke
def test_compare_scenes_passes_on_identical_scene():
    scene = _scene("settings/Overlay", "settings_detail", [("Item Names", 10, 10, 100, 20)])
    report = compare_scenes(scene, scene, SceneTolerance())
    assert report.ok, report.explain()


@pytest.mark.smoke
def test_compare_scenes_hard_fails_on_page_id_drift():
    rec = _scene("settings/Overlay", "settings_detail", [("Item Names", 10, 10, 100, 20)])
    rep = _scene("settings/Apps", "settings_detail", [("Item Names", 10, 10, 100, 20)])
    report = compare_scenes(rec, rep, SceneTolerance())
    assert not report.ok
    assert any("page_id" in f for f in report.failures)


@pytest.mark.smoke
def test_compare_scenes_hard_fails_on_scene_type_drift():
    rec = _scene(None, "unknown", [("Candy Crush Saga", 10, 10, 100, 20)])
    rep = _scene(None, "settings_detail", [("Candy Crush Saga", 10, 10, 100, 20)])
    report = compare_scenes(rec, rep, SceneTolerance())
    assert not report.ok
    assert any("scene_type" in f for f in report.failures)


@pytest.mark.smoke
def test_compare_scenes_tolerates_minor_text_jitter_but_fails_major():
    rows = [(f"row {i}", 10, 10 + 30 * i, 100, 20) for i in range(20)]
    rec = _scene("p", "settings_detail", rows)
    # one of 20 texts OCR-garbled: jaccard 19/21 ≈ 0.905 ≥ 0.85 → tolerated
    minor = _scene("p", "settings_detail", [*rows[:19], ("rovv 19", 10, 580, 100, 20)])
    report = compare_scenes(rec, minor, SceneTolerance())
    assert report.ok, report.explain()
    major = _scene("p", "settings_detail", [("garbled", 10, 10, 100, 20)])
    report = compare_scenes(rec, major, SceneTolerance(element_count_delta_max=10_000))
    assert not report.ok
    assert any("jaccard" in f for f in report.failures)


@pytest.mark.smoke
def test_compare_scenes_fails_on_moved_box():
    rec = _scene("p", "settings_detail", [("Camera", 10, 100, 100, 20)])
    rep = _scene("p", "settings_detail", [("Camera", 10, 600, 100, 20)])
    report = compare_scenes(rec, rep, SceneTolerance())
    assert not report.ok
    assert any("IoU" in f for f in report.failures)
